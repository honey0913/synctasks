  /* ── 공휴일 여부 확인 ───────────────────────── */
  isHoliday(dateStr) {
    return !!(this._HOLIDAYS[dateStr] || this._apiCache[dateStr]);
  },

  /* ── 공휴일 이름 반환 ───────────────────────── */
